# doctest

def add(a, b):
    """
    >>> add(4, 5)
    9
    >>> add(-1, -9)
    -10
    >>> add(-5, 3)
    -2
    """
    return a + b


def subtract(a, b):
    """
    >>> subtract(10, 5)
    5
    >>> subtract(-9, -7)
    -2
    >>> subtract(10, -9)
    19
    """
    return a - b


def multiply(a, b):
    """
    >>> multiply(6, 2)
    12
    >>> multiply(-8, 4)
    -32
    >>> multiply(5, 'm')
    'mmmmm'
    """
    return a * b


